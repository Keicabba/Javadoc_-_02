package it.figuccia;

/**
 * This class provides methods for performing basic arithmetic operations
 * @author Figuccia
 */
public class ArithmeticOperators {
    /**
     * Sums two integers
     * @param num1 the first integer
     * @param num2 the second integer
     * @return the summation of the two integers
     */
    int sum(int num1, int num2) {
        return num1 + num2;
    }
    /**
     * Subtracts two integers
     * @param num1 the first integer
     * @param num2 the second integer
     * @return the subtraction of the two integers
     */
    int sub(int num1, int num2) {
        return num1 - num2;
    }
    /**
     * Multiplies two integers
     * @param num1 the first integer
     * @param num2 the second integer
     * @return the multiplication of the two integers
     */
    int mul(int num1, int num2) {
        return num1 * num2;
    }
    /**
     * Divides two integers
     * @param num1 the first integer
     * @param num2 the second integer
     * @return the division of the two integers
     */
    int div(int num1, int num2) {
        return num1 / num2;
    }
}
